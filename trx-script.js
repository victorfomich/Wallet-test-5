async function loadTrxBalance() {
    try {
        console.log('üí∞ –ó–ê–ì–†–£–ñ–ê–ï–ú TRX –ë–ê–õ–ê–ù–°...');
        
        // –ü–æ–ª—É—á–∞–µ–º Telegram ID –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
        let telegramId = null;
        if (tg && tg.initDataUnsafe && tg.initDataUnsafe.user) {
            telegramId = tg.initDataUnsafe.user.id;
        } else {
            telegramId = 123456789; // –¢–µ—Å—Ç–æ–≤—ã–π ID
        }
        
        const response = await fetch(`/api/admin/balances?telegram_id=${telegramId}`);
        
        if (!response.ok) {
            throw new Error(`HTTP error! status: ${response.status}`);
        }
        
        const data = await response.json();
        console.log('üìä –ü–û–õ–£–ß–ï–ù TRX –ë–ê–õ–ê–ù–°:', data);
        
        if (data.success && data.balance) {
            updateTrxDisplay(data.balance);
        } else {
            console.log('‚ö†Ô∏è TRX –±–∞–ª–∞–Ω—Å –Ω–µ –Ω–∞–π–¥–µ–Ω');
            setDefaultTrxDisplay();
        }
        
    } catch (error) {
        console.error('üí• –û–®–ò–ë–ö–ê –ó–ê–ì–†–£–ó–ö–ò TRX:', error);
        setDefaultTrxDisplay();
    }
}

// –û–ë–ù–û–í–ò–¢–¨ –û–¢–û–ë–†–ê–ñ–ï–ù–ò–ï TRX
function updateTrxDisplay(balance) {
    const balanceElement = document.getElementById('balanceAmount');
    const trxElement = document.getElementById('trxBalance');
    
    if (balance.trx_amount !== undefined && balance.trx_price !== undefined) {
        // USD —Å—Ç–æ–∏–º–æ—Å—Ç—å
        const usdValue = balance.trx_amount * balance.trx_price;
        if (balanceElement) {
            balanceElement.textContent = `$${usdValue.toFixed(2)}`;
            console.log(`‚úÖ USD –°–¢–û–ò–ú–û–°–¢–¨: $${usdValue.toFixed(2)}`);
        }
        
        // –ö–æ–ª–∏—á–µ—Å—Ç–≤–æ TRX
        if (trxElement) {
            trxElement.textContent = `${balance.trx_amount.toFixed(6)} TRX`;
            console.log(`‚úÖ –ö–û–õ–ò–ß–ï–°–¢–í–û TRX: ${balance.trx_amount.toFixed(6)}`);
        }
        
        // –û–ë–ù–û–í–õ–Ø–ï–ú –ü–ï–†–ï–ú–ï–ù–ù–´–ï –î–õ–Ø –ì–õ–ê–ó–ò–ö–ê
        updateOriginalBalances();
        
    } else {
        console.log('‚ö†Ô∏è –ù–ï–¢ –î–ê–ù–ù–´–• TRX –í –ë–ê–õ–ê–ù–°–ï');
        setDefaultTrxDisplay();
    }
}

