                balanceAmount.classList.add('hidden');
                detailedBalance.textContent = '‚Ä¢‚Ä¢‚Ä¢ TRX';
                toggleEyeIcons(true);
                eyeIcon.classList.add('hidden');
                eyeIconDark.classList.add('hidden');
                
                console.log('üëÅÔ∏è –°–ö–†–´–õ–ò –ë–ê–õ–ê–ù–°');
